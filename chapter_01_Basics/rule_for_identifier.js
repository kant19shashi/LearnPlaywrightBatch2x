// Rules for Identifiers in JavaScript

// 1. Start with: A letter (a-z, A-Z), underscore (_), or dollar sign ($).
// 2. Contain: Letters, digits (0-9), underscores, or dollar signs only.
// 3. No spaces or special characters: Except _ and $.
// 4. Not a keyword: Cannot use reserved words like `var`, `if`, `function`.
// 5. Case-sensitive: `myVar` and `myvar` are different.
// 6. Length: No limit, but keep them descriptive and readable.

// Examples:
// Valid: `userName`, `_temp`, `$price`, `myVar123`
// Invalid: `123abc` (starts with digit), `my-var` (hyphen), `var` (keyword)